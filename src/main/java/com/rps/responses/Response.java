package com.rps.responses;

/**
 *
 * @author Ayush Verma
 * Default response structure for game
 */
public class Response {
	/** Response message */
	private String message;

	/**
	 * Getter for message
	 * @return message
	 */
	public String getMessage() {
		return message;
	}

	/**
	 * Setter for message
	 * @param message
	 */
	public void setMessage(String message) {
		this.message = message;
	}
}
